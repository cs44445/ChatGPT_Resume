<template>
    <div class="box-dist">
        <div style="display: flex;justify-content: end;">
        <a-button @click="handleNewServiceType" type="primary" icon="plus"  :disabled="formDisabled">新建服务类型</a-button>
        </div>
        <a-table ref="table" size="small" :scroll="{ x: true }" rowKey="id" :columns="serviceColumns" :dataSource="serviceData"
            :pagination="ipagination" :loading="loading" class="j-table-force-nowrap" @change="handleTableChange">

            <span slot="action" slot-scope="text, record">
                <a-popconfirm title="确定删除吗?" @confirm="() => deleteService(record.id)">
                    <a :disabled="formDisabled">删除</a>
                </a-popconfirm>
            </span>

        </a-table>

        <bus-service-type-modal ref="modalTypeForm" @ok="modalTypeForm"></bus-service-type-modal>
    </div>
</template>
<script>
import { getAction, httpAction, deleteAction } from '@/api/manage'
import BusServiceTypeModal from '../BusServiceTypeModal'
import '@/assets/less/TableExpand.less'
import { mixinDevice } from '@/utils/mixin'
import { JeecgListMixin } from '@/mixins/JeecgListMixin'
export default {
    props: {
        projectId: {
            type: String
        }
    },
    components: {
        BusServiceTypeModal
    },
    mixins: [JeecgListMixin, mixinDevice],
    data() {
        return {
            url: {
                serviceList: '/bus/busCompanyService/list',
                deleteService: '/bus/busCompanyService/delete',
            },
            serviceColumns: [
                {
                    title: '服务类型',
                    align: "left",
                    dataIndex: 'type_dictText'
                },
                // {
                //     title: '服务评级',
                //     align: "left",
                //     dataIndex: 'evaluate'
                // },
                {
                    title: '操作',
                    dataIndex: 'action',
                    align: "center",
                    fixed: "right",
                    width: 147,
                    scopedSlots: { customRender: 'action' }
                }
            ],
            serviceData: [],//银行数据
        }
    },
    watch: {
        projectId: function () {
            // this.loadData(this.projectId, '1', 1)
            this.getServiceList()
        }
    },
    computed: {
    },
    mounted() {
        // this.getServiceList()
    },
    methods: {
        //获取银行信息
        getServiceList() {
            getAction(this.url.serviceList, { companyId: this.projectId }).then((res) => {
                if (res.success) {
                    this.serviceData = res.result.records
                } else {
                    this.$message.warning(res.message)
                }
            })
        },
        handleNewServiceType(){
            let params={
                servicelist:true,
                projectId:this.projectId
            }
            this.$refs.modalTypeForm.add(params)
            this.$refs.modalTypeForm.title = "添加服务类型";
            this.$refs.modalTypeForm.disableSubmit = false;
            this.$refs.modalTypeForm.ifShowCompany = false;

        },
        modalTypeForm(){
            this.getServiceList()
            this.$emit('ok');
        },
        deleteService(id){
            deleteAction(this.url.deleteService, { id: id }).then((res) => {
                if (res.success) {
                    this.getServiceList()
                    this.$message.success(res.message)
                    this.$emit('ok')
                } else {
                    this.$message.warning(res.message)
                }
            })
        }
    }
}
</script>
